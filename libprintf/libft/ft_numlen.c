#include "libft.h"

size_t		ft_numlen(int nb)
{
	size_t	ret;

	ret = 0;
	if (nb == 0)
		return (1);
	if (nb < 0)
	{
		nb *= -1;
		ret++;
	}
	while (nb > 0)
	{
		nb = nb / 10;
		ret++;
	}
	return (ret);
}
